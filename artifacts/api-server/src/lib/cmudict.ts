export function lookupWord(word: string): { word: string; pronunciations: string[]; found: boolean } {
  const lower = word.toLowerCase().replace(/[^a-z']/g, "");
  const pronunciations = CMUDICT[lower];
  if (pronunciations && pronunciations.length > 0) {
    return { word, pronunciations, found: true };
  }
  return { word, pronunciations: [], found: false };
}

export function lookupText(text: string): Array<{ word: string; pronunciations: string[]; found: boolean }> {
  const words = text.trim().split(/\s+/).filter(Boolean);
  return words.map(lookupWord);
}
